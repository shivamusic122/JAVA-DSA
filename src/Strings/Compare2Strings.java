package Strings;

public class Compare2Strings {
    public static void main(String[] args) {
        String fname="tony";
        String lname="tony2";
        
        if(fname.compareTo(lname)==0){
            System.out.println("Strings are equal");
        }
        else{
            System.out.println("Strings are not equal");
        }

    }
}
